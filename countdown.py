#Using while Loop to Count Down from 10

#Step1:	Initialize a variable count with the value 10.
count = 10
 
#Step2:	Create a while loop that continues until count is less than 1. 
while count >= 1:
    print(count)    # Step3:Inside the loop, print the current value of count.
    count -= 1      # Step4: Decrement the value of count by 1 at the end of each loop iteration.


    

