# . Using for Loop to Iterate over a List
# Step1:	Create a list of numbers, numbers = [1, 2, 3, 4, 5].

numbers = [1,2,3,4,5]

# Step2: Use a for loop to iterate over each element in the numbers list.

for i in numbers:
    print(i," - ",i*i)    # Step3: Inside the loop, print each element and its square (multiply it by itself).

