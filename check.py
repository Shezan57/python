#  Using if Statement to Check Conditions
# Step1:	Ask the user to enter a number.
# Step2:	Store the input in a variable, num.

num = input("Enter a number: ")

# Step3:	Use an if statement to check if num is greater than 10.
if int(num) > 10:
    print("The number is greater than 10")  # Step4:  If it is, print "The number is greater than 10." 
else:
    print("The number is not greater than 10") # Step5:  If it isn't, print "The number is not greater than 10."

